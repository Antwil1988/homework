# Создание множества с разными типами данных
my_set = {1, "строка", 3.14, 1, }

# Вывод множества на экран (должны отобразиться только уникальные значения)
print("Set:", my_set)

# Добавление двух новых элементов
my_set.add("текст")
my_set.add(2023)

# Удаление одного элемента
my_set.remove("текст")

# Вывод измененного множества на экран
print("Modified set:", my_set)

